const ll mod = 1000000007;
/*
0
4
36
288
2400
389456655
*/

void solve()
{
	ll n; cin>>n;
	ll ans=1;
	for (ll i = 2; i <= n; ++i)
	{
		ans = ((ans%mod)*(i%mod))%mod;
	}
	ans = (ans%mod)*(n%mod);
	ans = (ans%mod)*((n-1)%mod);
	ans%=mod;
	cout<<ans<<nn;
}

int main()
{
    ll testcase;
    testcase = 1;
    cin >> testcase;
    while (testcase--)
    {
		solve();
    }
    return 0;
}